#include <iostream>
#include <vector>

int кількість_у_діапазоні(const std::vector<double>& масив, double A, double B) {
    int кількість = 0;
    for (double елемент : масив) {
        if (елемент >= A && елемент <= B) {
            кількість++;
        }
    }
    return кількість;
}

double сума_після_максимального(const std::vector<double>& масив) {
    if (масив.empty()) {
        return 0;
    }

    double максимальний = масив[0];
    int індекс_максимального = 0;

    // Знаходимо максимальний елемент та його індекс
    for (int індекс = 1; індекс < масив.size(); індекс++) {
        if (масив[індекс] > максимальний) {
            максимальний = масив[індекс];
            індекс_максимального = індекс;
        }
    }

    double сума = 0;
    // Обчислюємо суму елементів, які знаходяться після максимального
    for (int індекс = індекс_максимального + 1; індекс < масив.size(); індекс++) {
        сума += масив[індекс];
    }
    return сума;
}

int main() {
    std::vector<double> мій_масив = {2.5, 3.8, 1.2, 5.6, 4.3, 6.7}; // Зразок масиву, який можна змінити
    double A = 2.0;
    double B = 5.0;

    int кількість_у_діапазоні = кількість_у_діапазоні(мій_масив, A, B);
    std::cout << "Кількість елементів у діапазоні від " << A << " до " << B << ": " << кількість_у_діапазоні << std::endl;

    double сума_після_максимального_елемента = сума_після_максимального(мій_масив);
    std::cout << "Сума елементів після максимального елемента: " << сума_після_максимального_елемента << std::endl;

    return 0;
}
